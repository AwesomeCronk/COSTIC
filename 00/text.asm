; Text handling
; Contains the ASCII charset and functions for displaying text

; D = xpos
; E = ypos
; C = char
PutCharPx:
    push bc
    ld b, 0             ; BC = C but 16-bit
    push bc \ pop hl    ; HL = BC
    add hl, hl          ; HL *= 8
    add hl, hl
    add hl, hl
    ld bc, ASCII        ; BC = ASCII
    add hl, bc          ; HL += BC
    
    ld iy, $8100
	ld b, 8
	call PutSpriteOR
    pop bc

    ret

; D = xpos
; E = ypos
; C = char
; Destroys: A
PutChar:
    ld a, d     ; A = D
    add a, a    ; A *= 4
    add a, a
    add a, d    ; A += (D * 2)
    add a, d
    ld d, a     ; D = A

    ld a, e     ; A = E
    add a, a    ; A *= 8
    add a, a
    add a, a
    ld e, a

    call PutCharPx

    ret

